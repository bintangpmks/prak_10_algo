def bubbleSort(array):
    n = len(array)

    for i in range(n):

        for j in range(0, n-i-1):

            if array[j] > array[j+1] :
                array[j], array[j+1] = array[j+1], array[j]

def binarySearch(array, l, r, x):

    # Check base case
    if r >= l:

        mid = l + (r - l) // 2

        if array[mid] == x:
            return mid

        elif array[mid] > x:
            return binarySearch(array, l, mid-1, x)

        else:
            return binarySearch(array, mid + 1, r, x)

    else:
        return -1

